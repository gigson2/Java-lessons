package org.study;

public class datatypefloat {
    public static void main(String[] args) {
//       this is for float
        float max = 3.34352452352443e+38f;

        System.out.printf("%.2f",max);
        System.out.println(max);


//        this is for int division
        int x = 7/2;
        System.out.println(x);

        // double result
        double y = 7/2d;
        System.out.println(y);

        // boolean and char return true or false value
        boolean var = true;
        char sh = 's';
        System.out.println(sh);

    }
}
